/**
 * Builds an optimized word map from the training dataset.
 * Run: node scripts/build-word-map.js
 *
 * Output: src/utils/wordMap.js — a compact JS module with all word/phrase mappings
 * and extracted character+syllable mappings for the rule engine.
 */

import { readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');

// ── Read dataset ────────────────────────────────────────────────────
const raw = readFileSync(join(ROOT, 'marathi_transliteration_dataset.jsonl'), 'utf-8');
const lines = raw.split('\n').filter(Boolean);

const wordMap = {};
const syllableSet = new Set();
const seenSyllables = new Set(); // track unique (english, marathi) pairs

for (const line of lines) {
  try {
    const { english, marathi } = JSON.parse(line);
    if (!english || !marathi) continue;

    const key = english.toLowerCase().trim();
    if (wordMap[key] && wordMap[key] !== marathi) continue; // keep first occurrence
    wordMap[key] = marathi;

    // Track single-char and two-char segments as potential syllable rules
    if (english.length <= 4 && !english.includes(' ')) {
      const pair = `${english}→${marathi}`;
      if (!seenSyllables.has(pair)) {
        seenSyllables.add(pair);
        syllableSet.add(pair);
      }
    }
  } catch {
    // skip malformed lines
  }
}

// Parse syllable rules into structured data for the rule engine
// Format: consonant+vowel mappings and consonant–halant patterns
const consonantVowelMap = {};
const halantPatterns = {};

for (const entry of syllableSet) {
  const [eng, mar] = entry.split('→');

  // Pattern: consonantS+vowel → consonantM + matra (e.g., kaa→का, ki→कि)
  // Or: consonantS+consonantS+vowel → consonantM+halant+consonantM+matra (e.g., kakaa→कका)
  
  // Check if it's a simple C+V or 2C+V pattern
  if (eng.length === 2 || (eng.length === 3 && eng[1] === eng[0])) {
    // Could be consonant+vowel like "ka" or doubled like "kak"
    if (eng.length === 2) {
      const firstChar = eng[0];
      if (!consonantVowelMap[firstChar]) consonantVowelMap[firstChar] = {};
      // Store the mapping
      consonantVowelMap[firstChar][eng] = mar;
    }
  }
}

// ── Build pattern rules from the character-level data ──────────────
// Extract consonant base forms (C + 'a' = inherent vowel)
const consBase = {};
const consWithMatra = {};
const standaloneVowels = {};
const specialPatterns = {};

for (const [eng, mar] of Object.entries(wordMap)) {
  if (eng.length === 1 && mar.length === 1) {
    // Single char mapping - could be vowel or consonant base
    if ('aeiou'.includes(eng)) {
      standaloneVowels[eng] = mar;
    } else {
      consBase[eng] = mar;
    }
  } else if (eng.length === 2) {
    // Two characters: consonant+vowel pattern or double consonant
    if ('aeiou'.includes(eng[1])) {
      // C+V pattern
      const c = eng[0];
      const v = eng[1];
      if (!consWithMatra[c]) consWithMatra[c] = {};
      consWithMatra[c][v] = mar;
    } else if (eng[0] === eng[1]) {
      // Double consonant with inherent vowel (e.g., 'kk' → क्क)
      const c = eng[0];
      if (!halantPatterns[c]) halantPatterns[c] = {};
      halantPatterns[c][''] = mar; // no vowel = halant form
    }
  } else if (eng.length === 3 && eng[0] === eng[1] && 'aeiou'.includes(eng[2])) {
    // Pattern: C+C+V (e.g., 'kka' → क्क, 'kkaa' → क्का)
    const c = eng[0];
    const v = eng[2];
    if (!halantPatterns[c]) halantPatterns[c] = {};
    halantPatterns[c][v] = mar;
  }
}

// Also extract from longer patterns
for (const [eng, mar] of Object.entries(wordMap)) {
  if (eng.length === 3 && !'aeiou'.includes(eng[2])) {
    // Pattern like 'kak' → कक (CVC where last C has inherent 'a')
    if (eng[0] === eng[2]) {
      const c = eng[0];
      const v = eng[1] || 'a';
      if (!halantPatterns[c]) halantPatterns[c] = {};
      halantPatterns[c][v] = mar;
    }
  }
}

// ── Write output ──────────────────────────────────────────────────
const output = `/**
 * Auto-generated word map from the training dataset.
 * Generated by scripts/build-word-map.js
 * DO NOT EDIT MANUALLY — re-run the build script instead.
 */

// ═══════════════════════════════════════════════════════════════════════
//  Full word/phrase lookup (98,279 entries)
// ═══════════════════════════════════════════════════════════════════════

export const WORD_MAP = ${JSON.stringify(wordMap, null, 0)};

// ═══════════════════════════════════════════════════════════════════════
//  Character-level mapping rules (extracted from the dataset)
// ═══════════════════════════════════════════════════════════════════════

// Standalone vowel forms (when not preceded by a consonant)
export const VOWELS = ${JSON.stringify(standaloneVowels, null, 0)};

// Consonant base forms (with inherent 'a' vowel)
export const CONSONANTS = ${JSON.stringify(consBase, null, 0)};

// Consonant + matra (dependent vowel) forms
// Format: consMap[consonant][vowel] = devanagari_syllable
export const CONSONANT_MATRA_MAP = ${JSON.stringify(consWithMatra, null, 0)};

// Halant (consonant cluster) patterns
// Format: halantMap[consonant][vowel] = devanagari_with_halant
export const HALANT_MAP = ${JSON.stringify(halantPatterns, null, 0)};

// Number of words in the map
export const WORD_MAP_SIZE = ${Object.keys(wordMap).length};
`;

writeFileSync(join(ROOT, 'src', 'utils', 'wordMap.js'), output, 'utf-8');

// ── Stats ──────────────────────────────────────────────────────────
console.log('📊 Word map stats:');
console.log(`  Total entries: ${Object.keys(wordMap).length}`);
console.log(`  Phrases (with spaces): ${Object.keys(wordMap).filter(k => k.includes(' ')).length}`);
console.log(`  Single words: ${Object.keys(wordMap).filter(k => !k.includes(' ')).length}`);
console.log(`  Standalone vowels: ${Object.keys(standaloneVowels).length}`);
console.log(`  Consonant base forms: ${Object.keys(consBase).length}`);
console.log(`  Consonant+matra forms: ${Object.keys(consWithMatra).length} consonants`);
console.log(`  Halant patterns: ${Object.keys(halantPatterns).length} consonants`);
console.log(`  Word map file size: ~${(Buffer.byteLength(output) / 1024).toFixed(1)} KB`);
console.log('\n✅ wordMap.js generated successfully!');
